	<h2>Rahmenprogramm und Social Events</h2>

	<h4>OSM-Event</h4>

	<p>Am Sonntag, den 06. Juni findet von 10 bis 16 Uhr die beliebte OSM-Unconference als Online-Veranstaltung statt. Interessierte sind eingeladen daran teilzunehmen. Die Veranstaltung ist kostenfrei. Für das leibliche Wohl muss diesmal jeder selbst sorgen. Die Themensammlung erfolgt im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2021/OSM-Sonntag">OSM-Wiki</a>. Wir bitten um <a href="https://fossgis-konferenz.de/2021/anmeldung/">Anmeldung</a>.</p>
	

	<h4>Inoffizieller Start</h4>

	<p>Am Abend des 06. Juni laden wir Sie ein zum virtuellen "Warmup in der Netzwerkwelt". Nutzen Sie die Möglichkeit sich in der Onlineplattform umzuschauen und in der Netzwerkwelt andere Teilnehmende zu treffen. Bei Fragen finden Sie Hilfe am InfoDesk (Personen mit rotem Hut sind Helfer der FOSSGIS-Konferenz). Der Zugang wird in Ihrem Ticketbestelllink zur Verfügung gestellt. </p>
	
	<h4>Abendveranstaltung </h4>
	<p>Traditionell findet am ersten Abend der FOSSGIS-Konferenz die Abendveranstaltung statt. Auch wenn die FOSSGIS im Jahr 2021 ausschließlich online stattfindet, soll es für die Teilnehmenden die Möglichkeit geben sich bei einer Abendveranstaltung zu treffen. </p>

	<p>Schauen Sie gerne am 07.06.2021 von 20 -23 Uhr zur Abendveranstaltung "Netzwerkweltdialoge" rein. Der gesamte virtuelle Campus Rapperswil steht zum Flanieren und Treffen zu Verfügung.</p>

	<h4>Jeopardy-Quiz</h4>
	<p>Am <a href="https://fossgis-konferenz.de/2021/sessions/DSXKQN.php">Montag</a> und <a href="https://fossgis-konferenz.de/2021/sessions/AWSQZ7.php">Dienstag</a> gibt es nach dem regulären Konferenzprogramm um 17:30 Uhr ein humorvolles Quiz in Form des FOSSGIS-Jeopardy. Zum generellen Ablauf sowie zur Anmeldung als Teilnehmerin oder Teilnehmer finden sich <a href="https://fossgis-konferenz.de/2021/sessions/DSXKQN.php">Details im Programm</a>.

