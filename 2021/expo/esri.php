					<h3>Esri</h3>
					<p>Esri ist weltweiter Marktführer von Geoinformationssystemen, welche unter dem Namen ArcGIS als je eine Produktfamilie für den Cloud-, Server- und den Desktop-Bereich zusammengefasst werden. Anpassungsfähigkeit, Intuitivität und Integrationsfähigkeit kennzeichnen den Industriestandard ArcGIS. Mehr als eine Million Anwender weltweit wissen dies zu schätzen. Mit über 100 Niederlassungen in weltweit 67 Ländern unterstützt Esri Organisationen des privaten und öffentlichen Sektors, Big Data sowie Echtzeit- und raumbezogene Daten in gewinnbringende Informationen zu verwandeln, um so die operativen und geschäftlichen Ergebnisse zu verbessern.</p>

					<p>Für die FOSSGIS- und OSM-Community sind die OSM-Integration, OpenData-Schnittstellen und OS-APIs von OpenSource-Drittanbietern von grosser Bedeutung.</p>
					<p>In den letzten Jahren hat Esri OSM Daten in ArcGIS leichter zugänglich gemacht. Esri hostet eine Live-Replik der OpenStreetMap (OSM)-Daten und führte seit über zwei Jahren eine neue OpenStreetMap Vector Basemap ein. Die Karte wird ausschließlich mit OSM-Daten erstellt und ist für alle Benutzer frei verfügbar. Mit dem <u><a href="https://developers.arcgis.com/vector-tile-style-editor/">Vector Tile Style Editor</a></u> kann die OpenStreetMap-Basiskarte leicht angepasst werden. In Zusammenarbeit mit Facebook ermöglicht die aktuelle Version von <u><a href="https://wiki.openstreetmap.org/wiki/RapiD">RapiD</a> </u> und dem <u><a href="https://gitlab.com/gokaart/JOSM_MapWithAI/-/wikis/Testing-the-development-version">MapWithAI Plugin</a></u> den Zugang zu einer von Esri kuratierten Sammlung offener hochpräziser Datensätze - meist aus offiziellen Quellen. Dies bedeutet, dass für die Bearbeitung genauere Gebäudeumrisse oder Hausnummern, mit wenigen Klicks den OSM-Daten hinzugefügt werden können.</p>
	
					<p>ArcGIS unterstützt mit <u><a href="https://www.arcgis.com/index.html">ArcGIS Online</a></u> und <u><a href="https://hub.arcgis.com/">ArcGIS Hub</a></u> nicht nur die Interoperabilität aller wichtigen GIS-Datentypen, Formate und Spezifikationen von Daten inklusive den OGC Standards, sondern ermöglicht auch den Austausch und die Arbeit mit offenen Daten, um effektiv zu kommunizieren, vorgeschriebene Transparenzanforderungen zu erfüllen und einen besseren Einblick zu ermöglichen. Esri Inc. stellt zudem mit dem ArcGIS Living Atlas of the World die grösste Sammlung von geografischen Informationen aus aller Welt zur Verfügung. Für die Schweiz stehen standortbasierte offene Daten in der <u><a href="https://www.arcgis.com/home/search.html?q=owner%3A%22EsriSuisse_Content%22&t=content&restrict=false">Esri Schweiz Subskription</a></u> und für Deutschland in der <a href="https://www.arcgis.com/home/user.html?user=esri_DE_content">Esri Deutschland Subskription</a> und im <u><a href="https://opendata-esri-de.opendata.arcgis.com/">Esri Deutschland Open Data Portal</a></u> zur Verfügung.</p>

					<p>Zusätzlich zu den eigenen APIs und SDKs unterstützt ArcGIS die wichtigsten Open-Source-Drittanbieter mit Funktionen und Tutorials von Open-Source-JavaScript-Mapping-APIs. Die APIs erlaubt ArcGIS REST Services einfach und kostenlos zu nutzen. ArcGIS-Standortdienste mit Leaflet können durch ein leichtgewichtiges Set von Werkzeugen namens <a href="https://developers.arcgis.com/esri-leaflet/">Esri Leaflet</a> verwendet werden. Mit OpenLayers und ArcGIS Location Services können eine Reihe verschiedener Arten von <a href="https://developers.arcgis.com/openlayers/">Mapping-Anwendungen</a> entwickelt werden. Mit der <a href="https://developers.arcgis.com/mapbox-gl-js/">Mapbox GL JS und ArcGIS Location Services</a> können interaktive Karten aus Vector Tiles erstellt werden.</p>

			
