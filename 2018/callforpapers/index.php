                <h2>Call for Papers</h2>

                <p>Sie forschen, Sie entwickeln, Sie beschäftigen sich mit Open-Source-Geo-Software oder freien Geodaten? Sie haben neue Ideen in diesem Themenbereich oder sind bereits dabei, Freie Software und freie Geodaten zu nutzen oder zu entwickeln? Dann sind Sie auf unserer Konferenz — der FOSSGIS-Konferenz 2018 — genau richtig.</p>

                <dl>
                <dt>Wer wir sind:</dt>
                <dd>Die Konferenz wird vom <a href="https://fossgis.de">FOSSGIS e.V.</a> und der <a href="http://www.openstreetmap.de">OpenStreetMap Community</a> veranstaltet.</dd>

                <dt>Wir bieten:</dt>
                <dd>Eine Plattform für Ihre Idee, Ihr Projekt, Ihren Erfahrungsbericht auf der größten deutschsprachigen Anwenderkonferenz für freie Geoinformationssysteme und freie Geodaten. Im Jahr 2018 findet die FOSSGIS vom 21. bis 24. März an der Universität Bonn statt. Die Konferenz ist die größte deutschsprachige Anwender-Konferenz für Open-Source-Geo-Software und offene Geodaten. Der Call for Papers ist bis zum <strong>28.11.2017</strong> geöffnet.</dd>

                <dt>Wir suchen:</dt>
                <dd>Ihre Idee, Ihr Projekt, Ihr Erfahrungsbericht, Ihr Thema. Genauer gesagt, suchen wir Vorträge für Einsteiger und Fortgeschrittene, die spannende Themen behandeln und anregende Diskussionen auslösen. Vorträge zum Thema freie Geodaten, zum Beispiel OpenStreetMap oder Open Data sind ebenso möglich wie Beiträge zu Lösungen mit freier Software aus dem Bereich WebGIS, Desktop GIS, Geodatenbanken, Location-Based Services, etc.
                </dd>
                </dl>

                <p>Werden Sie Teil der Konferenz und bewerben Sie sich jetzt mit einem <strong>Vortrag</strong>, <strong>Lightning Talk</strong> oder <strong>Workshop</strong>.</p>

                <p>Die FOSSGIS-Konferenz ermöglicht drei verschiedene Vortragsformate:</p>
                <dl>
                    <dd>
                    <strong>Vortrag:</strong> 20-minütiger Vortrag z.B. Erfahrungsberichte oder neue Entwicklungen, anschließend 5 Minuten Zeit für Diskussionen und Fragen;
                    </dd>
                    <dd>
                        <strong>Lightning Talk:</strong> Kurze, knackige Auseinandersetzung mit einem Thema, welches in 5 Minuten, gern humorvoll, Anregung zur Diskussion bietet;
                    </dd>

                    <dd><strong>Workshop:</strong> Ein Workshop, für den 90 Minuten vorgesehen sind, soll den Teilnehmern ein Mix aus Theorie und Praxis vermitteln. Ein Workshop findet ab einer Teilnehmerzahl von 5 statt. Bei einer Workshop-Einreichung ist es wichtig, darauf zu achten, dass erreichbare Lernziele und notwendige Vorkenntnisse der Teilnehmer beschrieben sind. Ihr Text ist für die Teilnehmer_innen ausschlaggebend für die Entscheidung, den Workshop zu buchen, und wird im Programm veröffentlicht.</dd>
                </dl>

                <p>Bitte reichen Sie Ihren Abstract für die FOSSGIS 2018 ist ab sofort bis zum 28. November 2017 ein. Eine Fristverlängerung ist wegen der ohnehin straffen Zeitplanung nicht vorgesehen.</p>

                <p>Für alle Konferenzformate gilt, dass der Abstract ca. 1500 Zeichen umfassen und so dem Programmkomitee eine genaue Vorstellung über den geplanten Inhalt vermitteln soll. Beitragseinreichungen ohne Abstract müssen leider abgelehnt werden. Darüber hinaus bitten wir Sie, einen Kurztext mit bis zu 500 Zeichen für die Programmheftveröffentlichung zu schreiben.</p>

                <p>Bei Annahme Ihrer Einreichung freuen wir uns außerdem über einen Textbeitrag zu Ihrem Vortrag für die Veröffentlichung im Tagungsband.</p>

                <p>Über die Aufnahme Ihres eingereichten Vortrags werden Sie bis Mitte Januar 2018 durch das Programmkomitee informiert.</p>

                <p>Die FOSSGIS-Konferenz lebt von Ihren Beiträgen und Ihrem Besuch. Werden Sie Teil der FOSSGIS Konferenz 2018. Wir freuen uns auf Sie!</p>

