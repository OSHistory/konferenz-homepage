        <h3>Anreise</h3>
		 <p>Der <a href="https://www.openstreetmap.org/#map=17/50.72510/7.09125"> Konferenzstandort</a> ist gut an das ÖV Netz angebunden und schnell zu erreichen.</p>
		 <ul class="list">
			<li>1,5 km in die Altstadt von Bonn</li>
			<li>1 km zum Hauptbahnhof Bonn</li>
		 </ul></p>

		<h4>Anreise mit öffentlichen Verkehrsmitteln</h4>
         <p>Bonn ist sehr gut an das Netz der Deutschen Bahn angeschlossen sowie auch per Fernbus zu erreichen. 
			<i>Hinweis:</i> Durch Arbeiten an Oberleitungen und Gleisen wird es auf bestimmten Strecken zu Vollsperrungen kommen. Bitte informieren Sie sich über Ihre Verbindung. </p> 
		 <p>Die Nahverkehrsbuslinien 601, 602, 603 verbinden den Hauptbahnhof mit dem Konferenzstandort, Haltestelle <i>Bonn, Am Botanischen Garten</i></p>


        <h4>Anreise mit dem Auto</h4>
         <p> Die bequeme Anreise per Auto ist möglich, jedoch stehen nur sehr wenige Parkplätze zur Verfügung. Wenn Sie dringend einen Parkplatz am Konferenzstandort benötigen, kontaktieren Sie uns bitte <a href="mailto:konferenz-orga@fossgis.de">per E-Mail</a>.
Bitte beachten Sie, dass die Zufahrt zur City nur mit grüner Umweltplakette möglich ist.
		 <p><b>Eingabe ins Navigationsgerät</b></p>
		 <ul class="list">
		  <li> Straße: Meckenheimer Allee 176</li>
		  <li> Ort: Bonn</li>
		  <li> Postleitzahl: 53115</li>
		 </ul>
		 </p>


		 <p>Haben Sie schon einmal an die Möglichkeit von Carsharing oder Mitfahrgelegenheit bei<a href="https://www.blablacar.de/mitfahren/nach-bonn/" title="https://www.blablacar.de/" rel="nofollow"> BlaBlaCar</a> gedacht? Die Umwelt und Ihr Geldbeutel werden sich freuen.</p>


         <h4>Anreise mit dem Flugzeug</h4>
		  <p>Der Flughafen Köln/Bonn liegt circa 15 Kilometer nordöstlich der Stadt und ist über die A 59, eine Schnellbuslinie (SB60, zuschlagpflichtig) und die rechtsrheinische Bahnstrecke mit Bonn verbunden (Tarifverbund: VRS). Eine weitere Anbindung an den Luftverkehr existiert durch den Flugplatz Bonn-Hangelar, der in Sankt Augustin unmittelbar an der Grenze zum Stadtbezirk Beuel liegt. Der Flugplatz wird vorwiegend von Geschäftsreisenden und Sportfliegern genutzt </p>
