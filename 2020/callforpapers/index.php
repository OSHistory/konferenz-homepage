    <p>Folgen Sie uns auf <a href="https://twitter.com/FOSSGIS_Konf">Twitter</a> sowie auf der <a href="https://lists.fossgis.de/mailman/listinfo/fossgis-talk-liste">Mailingliste FOSSGIS-Talk</a>.</p>

