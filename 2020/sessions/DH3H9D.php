            <p>Geo-Daten können durch unterschiedlichste Sensoren günstig erfasst und über das Internet allen zur Verfügung gestellt werden. Insbesondere im Bereich der Biodiversitätsforschung ergeben sich dadurch neue Möglichkeiten Methoden zu entwickeln und Erkenntnisse zu gewinnen, um dem drohenden Biodiversitätsverlust entgegenzutreten. Voraussetzung hierfür ist aber ein konsequent umgesetztes Forschungsdatenmanagement, bei dem die raumbezogenen Daten nach den FAIR Prinzipien verwaltet werden. Dabei ist FAIR ein Akronym für findability, accessibility, interoperability, und reusability. In Deutschland soll deshalb in den kommenden Jahren eine nationale Forschungsdateninfrastruktur (NFDI) entstehen, um insbesondere die Geo-Datenbestände von Wissenschaft und Forschung systematisch zu erschließen, nachhaltig zu sichern und zugänglich zu machen sowie auf nationaler und internationaler Ebene interdisziplinär zu vernetzen. NFDI setzt dabei nicht nur auf die reine Datenerfassung, sondern ebenfalls auf die nachhaltige Entwicklung und Austausch von Softwarewerkzeugen, um damit Forschungsergebnisse nachvollziehbar und reproduzierbar zu machen. Hierbei sollen in diesem Beitrag Antworten auf die Frage gegeben werden, wie die FAIR-Prinzipien sich auf Softwarewerkzeuge übertragen lassen, die für die skalierbare Verarbeitung und Analyse von Geo-Daten eingesetzt werden.</p>
        <div class='abstract-bio'>
            <h3 class='abstract-bio-name'>Bernhard Seeger</h3>
            <p>Bernhard Seeger ist Professor für Datenbanksysteme an der Philipps-Universität Marburg und beschäftigt sich seit mehr als 30 Jahren mit dem Thema Geo-Datenbanken. Seine Arbeiten umfassen Entwicklungen im Bereich Geo-Indexstrukturen, wie z. B. den in vielen Systemen implementierten R*-Baum, und Geo-Joins als auch CEP-Systeme (Complex Event Processing) mit Geo-Daten. Seit 10 Jahren beschäftigt er sich mit dem Thema Forschungsdatenmanagement für Biodiversitätsdaten und hat innerhalb des Infrastrukturprojekts <a href="https://www.gfbio.org/">GFBio</a> ein Web-basiertes Werkzeug zur raum-zeitlichen Verarbeitung, explorativen Analyse und Visualisierung von BIG Spatial Data entwickelt. Er ist derzeit Mitantragsteller von NFDI4Biodiversity, in dem das Forschungsdatenmanagement im Bereich der Biodiversität als Teil der geplanten Nationalen Forschungsdateninfrastruktur (NFDI) weiterentwickelt werden soll.</p>
