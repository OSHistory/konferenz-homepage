            <p>Mit Trufi-App können Mapper und Entwickler eine multimodale Reiseplaner-App für ihre eigene Stadt, Region oder ihr Land erstellen. Insbesondere in Entwicklungsländern in Afrika, Mittel- und Südamerika sowie Südostasien, in denen hauptsächlich informeller Verkehr (keine Haltestellen, undokumentierte Strecken) fließt, aber auch hier in Deutschland. Für den Datenteil der öffentlichen Verkehrsmittel wird OSM zum Mappen der Routen verwendet. OSM2GTFS oder unsere eigenen Tools werden zum Erstellen von GTFS verwendet, welches wiederum in OpenTripPlanner gehostet wird, sodass aus der App Routen vorgeschlagen werden können.</p>
<p>Neben den offensichtlichen Aufgaben (Routen mappen, App-Code anpassen) müssen noch viele weitere wichtige Schritte ausgeführt werden: Team aufbauen, perfekte UX für die Stadt entwickeln, veröffentlichen, finanzieren, Marketing machen …</p>
<p>Technikseitig muss die Open-Source-Flutter-App customized werden und mit Maps versehen werden. Ich möchte praktisch aus unseren Implementierungen in Bolivien, Ghana, Äthiopien und Kolumbien berichten und was für eine Rolle OSM-Enthusiasten in Deutschland dabei spielen können. Abschließend möchte ich mit den Teilnehmern diskutieren, ob es gut oder schlecht ist, Hunderte von Busrouten in OSM zu speichern. Wir hatten dazu im Juli 2019 in der Talk-de-Mailingliste fruchtbare Diskussionen mit Vor- und Nachteilen darüber.</p>
