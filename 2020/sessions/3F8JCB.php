            <h1>Einleitung und Hintergrund</h1>
<p>Barrierefreiheit im öffentlichen Personennahverkehr (ÖPNV) — dies ist ein richtungsweisendes Ziel, das sich die Bundesrepublik Deutschland bis Anfang 2022 mit der Novellierung des Personenbeförderungsgesetzes gesetzt hat. Daraufhin wurden konkrete Anforderungen zur Datenerfassung im Rahmen des Projekts <a href="https://www.delfi.de/">DELFI+</a> entwickelt und den Arbeitsgruppen der regionalen, öffentlichen Verkehrsverbünde zur Umsetzung übermittelt. Insbesondere für kleine, in ländlichen Regionen agierende ÖPNV-Verbünde stellt die flächendeckende und lückenlose Erfassung von Haltestelleninformationen zur Barrierefreiheit mit den detaillierten Anforderungen allerdings eine nicht zu bewältigende finanzielle und personelle Herausforderung dar. Allein für Haltestellen sind dies über 48 zu erfassende Attribute pro Haltestellensteig. So hat beispielsweise der Verkehrsverbund Mittelsachsen mit ca. 9.000 Haltestellensteigen insgesamt 432.000 Datensätze zu erheben und zu pflegen! Zu den 48 Steigattributen kommen außerdem noch weitere 39 für die Erfassung von Wegen in und durch die Haltestelle hinzu. Schon diese Zahlen verdeutlichen die Brisanz, die mit dem Thema Datenerhebung verbunden ist.</p>
<h1>Projektziel</h1>
<p>Die Idee im Projekt OPENER ist es, Bürgerinnen und Bürger die Möglichkeit zu geben, sich bei der Datenerfassung zu beteiligen und somit „ihren Nahverkehr“ selbst mit zu verbessern.
Das technische Ziel unseres Projektes ist daher die Entwicklung und Bereitstellung einer Open-Data-Plattform zur DELFI+-konformen Erfassung, Bereitstellung und Bewertung von Daten zu Barrieren an Haltestellen des ÖPNV. Die erhobenen Daten dienen daraufhin zur Generierung baulicher Hilfestellungen mit dem Ziel der Erreichung von Barrierefreiheit. Im Projekt wird ein Erfassungswerkzeug entwickelt, das ohne aufwendige Mess- und Eingabeverfahren das Aufnehmen der Daten direkt an der Haltestelle ermöglicht. Ein offener Zugang zu Daten und Software soll daraufhin die Basis für neue Anwendungen sowie für Handlungsempfehlungen für Nutzer, Kommunen und Verkehrsverbünde sein.</p>
<h1>Vortragsinhalte</h1>
<h2>Entwickelte Plattform</h2>
<p>Es wurde eine Android-Applikation entwickelt, welche Haltestellensteige in OSM-Karten rendert und für jeden Steig die zu erfassenden DELFI-Attribute in Form von Frage-Dialogen an den Nutzer der App abbildet. Neben Entscheidungsfragen oder Fragen mit Texteingabe als Antwort beinhaltet der DELFI-Katalog Fragen zu Abmaßen, beispielsweise von Bahnsteighöhen oder Wegbreiten. Hierfür wurde in die App ein Vermessungswerkzeug basierend auf Google ARCore entwickelt, das eine unkomplizierte Vermessung dieser Objekte auch ohne Zollstock erlaubt. Die Android-Applikation ist als Open Source im <a href="https://gitlab.hrz.tu-chemnitz.de/opener/opener-app/">OPENER App-Repository</a> verfügbar.</p>
<p>Aktuell wird eine Serveranwendung umgesetzt, sodass die erfassten Daten in einer Datenbank hinterlegt werden können.
Dabei existiert für jeden Haltestellensteig eine weltweit eindeutige Identifikationsnummer, die sog. Deutsche Haltestellenidentifikationsnummer (DHID), welche aus dem Zentralen Haltestellenverzeichnis (ZHV) des DELFI e.V. in die Serveranwendung eingespeist wird.</p>
<p>Zu Projektende wird im Februar 2020 eine Applikation mit Serveranbindung vorliegen, welche eine ortbezogene Erhebung und Speicherung von Daten an Haltestellen ermöglicht.
Nun liegt es nahe, die von den Nutzern erhobenen Daten in OpenStreetMap zurückzuspielen.
An dieser Stelle möchten wir sowohl rechtliche als auch technische Aspekte präsentieren und zur Diskussion stellen.</p>
<h2>Rechtliche Aspekte</h2>
<p>Die bestehenden Daten aus dem ZHV des DELFI e.V. werden durch die Nutzung der OPENER-App veredelt. Konkret heißt dies, dass die vorhandene DHID mit ihren Steig-Koordinaten um die erfassten DELFI-Attribute angereichert werden. Bei der Rückspeisung ins OSM muss also die Lizenzkompatibilität sichergestellt werden. Wir befinden uns aktuell in Absprache mit dem DELFI e.V. über eine Überführung der erhobenen Daten in die Hoheit des Vereins. Bei diesem Schritt streben wir an, die erhobenen Daten unter eine ODbL-kompatible-Lizenz zu stellen.</p>
<h2>Technische Aspekte</h2>
<p>Für die erhobenen DELFI-Attribute haben wir sinnvolle äquivalente OSM-Tags recherchiert und im <a href="https://wiki.openstreetmap.org/wiki/User:OPENER/DELFI_OSM_Tags">OPENER OSM Wiki</a> die Zuordnung bestehender Tags zu den DELFI-Attributen aufgelistet.
Des Weiteren existiert in OSM bereits eine international eindeutige Nummer zur Referenzierung von ÖPNV-Haltestellen: die IFOPT (Identification of Fixed Objects in Public Transport).
Die flächendeckende Datenerhebung hinsichtlich Barrierefreiheit an Haltestellen könnte also unter Verwendung der IFOPT zusammen mit den vorgeschlagenen, äquivalenten Tags erfolgen, indem fehlende Informationen in OSM um die mittels der App erhobenen Daten ergänzt wird. Diese Daten können aus der OPENER-Datenbank extrahiert und in die OSM-Datenbank importiert werden. Dazu werden im Vortrag Import-Konzepte präsentiert und zur Diskussion gestellt.</p>
