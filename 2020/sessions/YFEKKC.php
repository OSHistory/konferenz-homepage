            <p>Zu den Aufgabenbereichen des Bundesamtes für Strahlenschutz (BfS) gehört es, bei einem radiologischen Notfall, eigene und anderweitig verfügbare relevante Daten zu sammeln und zu erfassen, zu verarbeiten und zu bewerten sowie Dokumente zu erstellen, die die notwendigen Informationen enthalten, um den Krisenstab zu befähigen, über geeignete Maßnahmen des Notfallschutzes zu entscheiden. Der dabei eingesetzte Softwarestack wird unter der Bezeichnung IMIS (integriertes Mess- und Informationssystem) betrieben. Nach mehreren Jahren Entwicklungszeit wurde das bisherige proprietäre System (IMIS2) durch ein konsequent auf freien Komponenten und mit deutlicher Erweiterung um räumliche Funktionen und jetzt web-basierendes System ersetzt. Das neue IMIS3 wird von nun an bei der Bewältigung radiologischer Ereignisse und Notfälle vom radiologischen Lagezentrum des Bundes eingesetzt. Die bei der Neuentwicklung des IMIS3 konsequent eingesetzte Open-Source-Strategie kombiniert mehrere OSGeo-Projekte und hat einerseits einigen Input in diese Projekte geliefert und andererseits weitere Projekte neu entstehen lassen, die vom BfS unter der GPL auf github.com veröffentlicht wurden. Diese offensive Vorgehensweise ist für ein Bundesamt wohl eher ungewöhnlich.
Im Vortrag wird die Architektur des IMIS3 im Bezug auf die verwendeten Open-Source-GIS-Komponenten dargestellt. Diese enthält zum einen den aus OpenLayers und GeoExt aufgebauten generischen Web-GIS-Klienten des BfS. Aussehen, Inhalt und Funktionalität des Web-GIS wird über ein eigenes in Geonetwork OS gepflegtes Metadatenschema gesteuert. Die Generik des Klienten erlaubt es diesen auch für andere Fachbereiche mit vergleichbaren Datenstrukturen abseits des radiologischen Notfallschutzes einzusetzen. Die Machbarkeit einer solchen universellen Verwendung wird im WS 2019/2020 im Rahmen eines Studierendenprojekts an der Hochschule Karlsruhe geprüft und dessen Ergebnisse und Erfahrungen im Vortrag mit eingebaut. Zudem kommen bei IMIS3 noch PostGIS, Geostyler, Mapfish Print sowie diverse andere Projekte zum Einsatz, die das Gesamtsystem zur Bewältigung radiologischer Notfälle befähigt.</p>
        <div class='abstract-bio'>
            <h3 class='abstract-bio-name'>Marco Lechner</h3>
            <p>Marco Lechner arbeitet beim Bundesamt für Strahlenschutz im Fachgebiet Koordination Notfallschutzsysteme und koordiniert dabei die Software-Entwicklung des Integrierten Mess- und Informationssystems (IMIS). Dies beinhaltet zudem die Realisierung internationaler und nationaler Daten- und Dokumentenflüsse in heterogenen Strukturen sowie Fragen der Verfügbarkeit und Sicherheit der Systeme in einem Not- oder gar Katastrophenfall.</p>
