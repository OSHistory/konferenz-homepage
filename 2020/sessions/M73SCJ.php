            <p>GeoStyler ist eine Open Source JavaScript-Bibliothek zur einfachen Erstellung von modernen
Web-Oberflächen zum kartographischen Stylen von Geodaten. Somit wird es dem Anwender
möglich ohne Programmierung und Editieren von Text-Dateien (XML und co.) Styling-Vorschriften
interaktiv zu gestalten und diese in diverse offene Style-Formate zu überführen.
Dazu können auch die zu stylenden Geodaten hochgeladen und verknüpft werden, so dass attribut-basierte Styles auf einfache Art erstellt werden können.</p>
<p>Aktuell werden im GeoStyler folgende Formate unterstützt:</p>
<p><strong>Style-Vorschriften</strong></p>
<ul>
<li>OGC SLD</li>
<li>OpenLayers Styles</li>
<li>QGIS Styles</li>
<li>Mapbox Styles</li>
</ul>
<p><strong>Geodaten-Formate</strong></p>
<ul>
<li>GeoJSON</li>
<li>OGC WFS</li>
<li>Shapefile</li>
</ul>
<p>Auf der FOSSGIS 2019 wurde das GeoStyler Projekt bereits präsentiert. In diesem Jahr sollen die Neuerungen des letzten Jahres anhand aktueller Beispiele, in denen der GeoStyler zum Einsatz kommt, präsentiert werden. Dazu gehört die u. a. die GeoStyler GeoServer Extension, die es erlaubt die Stile der GeoServer Layer direkt im GeoServer mittels graphischer Oberfläche zu editieren. Außerdem wird es einen Überblick über zukünftige Entwicklungen im Projekt gegeben.</p>
<p>Quellcode auf github: https://github.com/geostyler/geostyler</p>
<p>Online-Dokumentation: https://geostyler.github.io/geostyler/latest/index.html</p>
<p>Demo: https://www.geostyler.org</p>
