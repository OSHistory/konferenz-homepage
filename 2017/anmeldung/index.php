                <h2>Anmeldung zur Konferenz</h2>
                <p>Wir laden Sie ein Teil der FOSSGIS-Konferenz 2017 zu werden.  Denn die Konferenz lebt von Ihren Beitr&auml;gen und Ihrem Besuch. Wir freuen uns auf Sie!</p>
                <p>In Erwartung von &uuml;ber 300 Teilnehmenden ist eine Registrierung Ihrerseits notwendig und hilfreich f&uuml;r die Organisation. Nach erfolgreicher Anmeldung erhalten Sie eine Rechnung. Nach &Uuml;berweisung des Rechnungsbetrages sind Sie registriert. Im Rahmen der Anmeldung können Sie bis zum 19.03.2017 eine Reihe von FOSSGIS-Workshops (kostenpflichtig) auswählen. Informationen zu den einzelnen Workshops finden Sie im <a href="https://www.fossgis-konferenz.de/2017/programm/">Programm</a>. </p>
				<p>Eine spontane Teilnahme ist m&ouml;glich, bitte melden Sie sich bei Ankunft am Welcome Desk. Vor Ort kann nur in Bar bezahlt werden.</p>

				<h3>Preise FOSSGIS-Konferenz 2017</h3> 
				<ul class="list">
				<li> Standardticket (Normalpreis): € 190</li>
				<li> Standardticket (Frühbucherrabatt bis 13.02.2017, 21 Uhr): € 150</li>
				<li> Beitragendenticket: € 120</li>
				<li> Studierendenticket: € 100</li>
				<li> Communityticket*: € 0*</li>
				<li> Workshop: € 100</li>
				<li> Workshop (Frühbucherrabatt bis 13.02.2017, 21 Uhr): € 90</li>
				</ul>
                <p><b>Im Konferenzticket enthalten:</b> FOSSGIS-Konferenz-Teilnahme, Pausensnack, Tagungsband, Tasche, Dialoge am Inn.<br>
* Regelungen für das Communityticket gelten für FOSSGIS-, Openstreetmap- und OSGEO-Aktive, bitte bei Anmeldung im "Freitext für FOSS/OSM-Community Aktive" darstellen.</p>

                <h3>Helfer</h3>
                <p>Freiwillige Helfer sind eingeladen und willkommen während der Konferenz bei den Videoaufnahmen zu unterstützen. Bei Interesse bitte bei per E-Mail unter konferenz-orga@fossgis.de melden und Zeitraumpräferenz angeben.</p>


